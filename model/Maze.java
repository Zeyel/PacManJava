	//CONSTRUCTEURS
	
	public Maze (World m) {
		this.world = m;
		
		this.height = maze.length;
		this.width = maze[0].length;
		
		ElementCreator wall =  new ElementCreatorWall(null);
		ElementCreator path = new ElementCreatorPath(wall);
		ElementCreator crossing = new ElementCreatorCrossing(path);
		elementCreatorCOR = new ElementCreatorFantomWall(crossing);
		
		this.init();
	}
	
	//GETTERS & SETTERS
	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
	
	public Element getElement(int x, int y) {
		return mazeElement[x][y];
	}
	
	public int getLabyElem(int x, int y) {
		return this.maze[x][y];
	}
	
	public Pellet getPellet(int x, int y) {		
		return pelletsElement[x][y];
	}
	
	//METHODS
	
	private void init() {
	
		mazeElement = new Element[height][width];
		pelletsElement = new Pellet[height][width];
		
		for(int i = 0; i < height ; i++) {
			for(int j = 0; j < width ; j++) {
				Element elem = elementCreatorCOR.handle(world, i, j, maze[i][j]);
				mazeElement[i][j] = elem;
				
				if(pellets[i][j] != none) {
					pelletsElement[i][j] = new Pellet(world,i,j);
					
					if(pellets[i][j] == superPellet) {
						pelletsElement[i][j].setSuper();
					}				
				}
				else {
					pelletsElement[i][j] = null;
				}
			}			
		}
		
	}

	@Override
	public Iterator<Element> iterator() {
		return new MazeIterator(this);
	}
	
